-- STEP 4: 최종 검증
-- ============================================================

-- [4-1] 모든 TIMESTAMP 컬럼 타입 확인 → 전부 WITH TIME ZONE이어야 함
SELECT TABLE_NAME, COLUMN_NAME, DATA_TYPE
FROM USER_TAB_COLUMNS
WHERE DATA_TYPE LIKE 'TIMESTAMP%'
ORDER BY TABLE_NAME, COLUMN_NAME;
-- 예상: 14개 전부 "TIMESTAMP(6) WITH TIME ZONE"

-- [4-2] 데이터 정합성 확인 — 백업과 비교
-- TASK_COMMENTS: 건수 동일한지
SELECT
  (SELECT COUNT(*) FROM TASK_COMMENTS) as CURRENT_CNT,
  (SELECT COUNT(*) FROM TASK_COMMENTS_BACKUP) as BACKUP_CNT
FROM DUAL;

-- TEAM_TASKS: 건수 동일한지
SELECT
  (SELECT COUNT(*) FROM TEAM_TASKS) as CURRENT_CNT,
  (SELECT COUNT(*) FROM TEAM_TASKS_BACKUP) as BACKUP_CNT
FROM DUAL;

-- [4-3] startAt/endAt 보정 결과 최종 확인
SELECT TASK_ID,
       TO_CHAR(START_AT, 'YYYY-MM-DD HH24:MI:SS TZH:TZM') as START_AT,
       TO_CHAR(END_AT, 'YYYY-MM-DD HH24:MI:SS TZH:TZM') as END_AT
FROM TEAM_TASKS
WHERE TASK_ID IN (601, 602)
ORDER BY TASK_ID;
-- 601: START_AT = +09:00 (변경 없음)
-- 602: START_AT = 07:54 +00:00 (보정 완료)

-- [4-4] offset 분포 최종 확인
SELECT * FROM (
  SELECT 'TEAM_TASKS.START_AT' as COL, EXTRACT(TIMEZONE_HOUR FROM START_AT) as TZ, COUNT(*) as CNT
  FROM TEAM_TASKS WHERE START_AT IS NOT NULL GROUP BY EXTRACT(TIMEZONE_HOUR FROM START_AT)
  UNION ALL
  SELECT 'TEAM_TASKS.END_AT', EXTRACT(TIMEZONE_HOUR FROM END_AT), COUNT(*)
  FROM TEAM_TASKS WHERE END_AT IS NOT NULL GROUP BY EXTRACT(TIMEZONE_HOUR FROM END_AT)
  UNION ALL
  SELECT 'TASK_COMMENTS.CRTD_AT', EXTRACT(TIMEZONE_HOUR FROM CRTD_AT), COUNT(*)
  FROM TASK_COMMENTS GROUP BY EXTRACT(TIMEZONE_HOUR FROM CRTD_AT)
) ORDER BY 1, 2;


