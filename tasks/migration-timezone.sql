ALTER TABLE TEAM_TASKS DROP COLUMN COMPLETED_AT;
ALTER TABLE TEAM_TASKS RENAME COLUMN COMPLETED_AT_NEW TO COMPLETED_AT;

-- 확인
SELECT TASK_ID, TO_CHAR(COMPLETED_AT, 'YYYY-MM-DD HH24:MI:SS TZH:TZM') as COMPLETED_AT
FROM TEAM_TASKS WHERE COMPLETED_AT IS NOT NULL FETCH FIRST 3 ROWS ONLY;

-- --------------------------------------------------------
-- [3-5] TEAM_TELEGRAM_LINKS.CRTD_AT (NOT NULL, DEFAULT CURRENT_TIMESTAMP)
-- --------------------------------------------------------
ALTER TABLE TEAM_TELEGRAM_LINKS ADD (CRTD_AT_NEW TIMESTAMP(6) WITH TIME ZONE);

